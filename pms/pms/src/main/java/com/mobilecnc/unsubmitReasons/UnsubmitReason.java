package com.mobilecnc.unsubmitReasons;

import java.util.Date;

public class UnsubmitReason {

	public String unsubmitReasonID;
	public String reason;
	public String description;
	public Date entryDate;
	public String entryBy;
	public Date lastModifyDate;
	public String lastModifyBy;
	public String getUnsubmitReasonID() {
		return unsubmitReasonID;
	}
	public void setUnsubmitReasonID(String unsubmitReasonID) {
		this.unsubmitReasonID = unsubmitReasonID;
	}
	public String getReason() {
		return reason;
	}
	public void setReason(String reason) {
		this.reason = reason;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public Date getEntryDate() {
		return entryDate;
	}
	public void setEntryDate(Date entryDate) {
		this.entryDate = entryDate;
	}
	public String getEntryBy() {
		return entryBy;
	}
	public void setEntryBy(String entryBy) {
		this.entryBy = entryBy;
	}
	public Date getLastModifyDate() {
		return lastModifyDate;
	}
	public void setLastModifyDate(Date lastModifyDate) {
		this.lastModifyDate = lastModifyDate;
	}
	public String getLastModifyBy() {
		return lastModifyBy;
	}
	public void setLastModifyBy(String lastModifyBy) {
		this.lastModifyBy = lastModifyBy;
	}
}
